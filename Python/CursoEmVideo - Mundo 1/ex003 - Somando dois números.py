# Crie um script Python que leia dois numeros e tente mostrar a soma entre eles

a = input('Vamos somar! Digite um numero. ')    # a = int(input('Vamos somar! Digite um numero. '))
b = input('Digite outro numero ')               # b = int(input('Digite outro numero '))
soma = int(a) + int(b)                          # soma = a + b
print('A soma destes números é {}'.format(soma))
